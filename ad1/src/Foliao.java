public class Foliao {
    private String nome;
    private int idade;
    private String estilo;


    public void setNome(String foliao){
        this.nome = foliao;
    }

    public String getNome(){
        return this.nome;
    }


    public void setIdade(int foliao_idade){
        this.idade = foliao_idade;
    }

    public int getIdade(){
        return this.idade;
    }


    public void setEstilo(String estilo_foliao){
        this.estilo = estilo_foliao;
    }

    public String getEstilo(){
        return this.estilo;
    }
}
