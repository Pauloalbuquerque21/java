public class ExemplosVariaveis {
    public static void main(String[] args) {
        int idade = 25;
        double altura = 1.75;
        //Você tem que colocar '' aspas simples.
        char genero = 'M';
        boolean ativo = true;
        String nome = "João";
        //você tem que usar aspas duplos "", quando é uma String. Colocamos letra maiusula na String, pois ela no java é uma class especial.
        String cpf = "12231354798558";

        System.out.println("idade = "+idade);
        System.out.println("Altura = "+altura);
        System.out.println("Gênero = "+genero);
        System.out.println("Ativo = "+ativo);
        System.out.println("Nome = "+nome);
        System.out.println("CPF = "+cpf);



    }
}
